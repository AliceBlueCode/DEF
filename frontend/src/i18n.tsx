const dicts: Record<Lang, Record<string, string>> = { ja, en }

// ── core translate function ─────────────────────────────────────────────────

export function toLang(v: string): Lang {
  return v === 'ja' ? 'ja' : 'en'
}

export function translate(key: string, lang: Lang, vars?: Record<string, string | number>): string {
  const str = dicts[lang][key] ?? dicts['ja'][key] ?? key
  if (!vars) return str
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replaceAll(`{${k}}`, String(v)),
    str
  )
}

// ── React context ───────────────────────────────────────────────────────────

type LanguageContextValue = {
  lang: Lang
  setLang: (l: Lang) => void
}

const LanguageContext = createContext<LanguageContextValue>({ lang: 'ja', setLang: () => {} })

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(
    () => (localStorage.getItem(LS_KEY_LANG) as Lang) || 'ja'
  )

  const setLang = (l: Lang) => {
    localStorage.setItem(LS_KEY_LANG, l)
    setLangState(l)
  }

  return <LanguageContext.Provider value={{ lang, setLang }}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  return useContext(LanguageContext)
}

export function useT() {
  const { lang } = useLanguage()
  return (key: string, vars?: Record<string, string | number>) => translate(key, lang, vars)
}
